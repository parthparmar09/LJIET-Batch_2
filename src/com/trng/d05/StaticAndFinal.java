package com.trng.d05;

// final -- Used to make a variable, method, or class unchangeable or non-extendable
// static  -- Used to declare a member (variable or method) that belongs to the class itself, rather than instances of the class
public class StaticAndFinal {
    final int num = 10;// can't be changed - a CONSTANT
    static int classId = 101;// belongs to the class
}
